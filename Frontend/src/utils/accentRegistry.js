const DEFAULT_ACCENT = "blue";

const normalizeAccentName = (accentName) => {
  const normalized = String(accentName ?? "")
    .trim()
    .toLowerCase();
  return accentRegistry[normalized] ? normalized : DEFAULT_ACCENT;
};

export const getAccentPalette = (accentName) => {
  const normalized = normalizeAccentName(accentName);
  return accentRegistry[normalized];
};

export const getAccentStyleVars = (accentName) => {
  const accent = getAccentPalette(accentName);

  return {
    "--accent-icon-bg": accent.iconBackground,
    "--accent-icon-color": accent.iconColor,
    "--accent-progress-fill": accent.progressFill,
    "--accent-progress-track": accent.progressTrack,
    "--accent-badge-bg": accent.badgeBackground,
    "--accent-badge-border": accent.badgeBorder,
    "--accent-badge-text": accent.badgeText,
    "--accent-hover-border": accent.hoverBorder,
    "--accent-hover-shadow": accent.hoverShadow,
  };
};

export default accentRegistry;
